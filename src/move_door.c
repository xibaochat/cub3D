#include "cub3D.h"

void	move_door(t_map *r)
{
	r->nb_door++;
	if (r->nb_door == 5)
		r->nb_door = 4;
	if (r->lucky_value != (r->all_spr + 3)->nb_occur)
		r->nb_door = 0;
}
